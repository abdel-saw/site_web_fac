    <!-- Counter Animation Script -->
    <script>
        document.addEventListener('DOMContentLoaded', (event) => {
            function animateCounter(element, start, end, step, duration) {
                let startTimestamp = null;
                const totalSteps = Math.ceil((end - start) / step);
                const stepDuration = duration / totalSteps;
                const count = () => {
                    if (start < end) {
                        start += step;
                        if (start > end) {
                            start = end;
                        }
                        element.innerText = start;
                        setTimeout(count, stepDuration);
                    }
                };
                count();
            }

            const counters = document.querySelectorAll('.counter');
            const stepValue = 5; // Fixed step value
            const duration = 1500; // Duration in milliseconds

            counters.forEach(counter => {
                const endValue = parseInt(counter.getAttribute('data-end'), 10);
                animateCounter(counter, 0, endValue, stepValue, duration);
            });
        });
    </script>
